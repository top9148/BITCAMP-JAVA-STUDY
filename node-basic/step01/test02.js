// 리터럴(literal) 표현

// 문자열
console.log("문자열")
console.log('문자열')

// 숫자
console.log(100)
console.log(3.14)

// 논리값
console.log(true)
console.log(false)

// 주소 
console.log(new Object()) // 객체 주소
console.log(function(){}) // 함수 객체(객체 + 함수코드) 주소
console.log(null) // null을 표현하는 객체

// 특별한 상수값
console.log(undefined) // 값이 없음을 의미
console.log(Infinity) // + 무한대
console.log(-Infinity) // - 무한대
console.log(NaN) // 숫자가 아님을 의미








